def hist2d(x, y, n=100):
    # 2d histogram used in labels.png and evolve.png
    xedges, yedges = np.linspace(x.min(), x.max(), n), np.linspace(y.min(), y.max(), n)
    hist, xedges, yedges = np.histogram2d(x, y, (xedges, yedges))
    xidx = np.clip(np.digitize(x, xedges) - 1, 0, hist.shape[0] - 1)
    yidx = np.clip(np.digitize(y, yedges) - 1, 0, hist.shape[1] - 1)
    return np.log(hist[xidx, yidx])


def butter_lowpass_filtfilt(data, cutoff=1500, fs=50000, order=5):
    # https://stackoverflow.com/questions/28536191/how-to-filter-smooth-with-scipy-numpy
    def butter_lowpass(cutoff, fs, order):
        nyq = 0.5 * fs
        normal_cutoff = cutoff / nyq
        b, a = butter(order, normal_cutoff, btype='low', analog=False)
        return b, a

    b, a = butter_lowpass(cutoff, fs, order=order)
    return filtfilt(b, a, data)  # forward-backward filter




def expand_bbox(bbox, img_w, img_h, expand_ratio):
    cx = (bbox[0] + bbox[2]) / 2
    cy = (bbox[1] + bbox[3]) / 2
    w = bbox[2] - bbox[0]
    h = bbox[3] - bbox[1]

    new_w = w * (1 + expand_ratio)
    new_h = h * (1 + expand_ratio)
    new_x1 = max(0, int(cx - new_w / 2))
    new_y1 = max(0, int(cy - new_h / 2))
    new_x2 = min(img_w, int(cx + new_w / 2))
    new_y2 = min(img_h, int(cy + new_h / 2))

    d_b0 = bbox[0] - new_x1
    d_b1 = bbox[1] - new_y1
    d_b2 = new_x2 - bbox[2]
    d_b3 = new_y2 - bbox[3]
    new_b0 = 0 + d_b0
    new_b1 = 0 + d_b1
    new_b2 = (new_x2 - new_x1) - d_b2
    new_b3 = (new_y2 - new_y1) - d_b3
    # [new_b0, new_b1, new_b2, new_b3]是相对外扩的crop图像中原始bbox的新坐标
    return [new_x1, new_y1, new_x2, new_y2], [new_b0, new_b1, new_b2, new_b3]


