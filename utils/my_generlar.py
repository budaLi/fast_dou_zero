def check_anchors(dataset, model, thr=4.0, imgsz=640):
    # Check anchor fit to data, recompute if necessary
    """
       计算默认锚点anchor与数据集标签框的长宽比值
       标签的长h宽w与anchor的长h_a宽w_a的比值, 即h/h_a, w/w_a都要在(1/hyp['anchor_t'], hyp['anchor_t'])是可以接受的
       如果标签框满足上面条件的数量小于总数的99%，则根据k-mean算法聚类新的锚点anchor
    """
    print('\nAnalyzing anchors... ', end='')
    m = model.module.model[-1] if hasattr(model, 'module') else model.model[-1]  # Detect()
    shapes = imgsz * dataset.shapes / dataset.shapes.max(1, keepdims=True)
    scale = np.random.uniform(0.9, 1.1, size=(shapes.shape[0], 1))  # augment scale 0.9-1.1的随机比率 增强label中wh
    wh = torch.tensor(np.concatenate([l[:, 3:5] * s for s, l in zip(shapes * scale, dataset.labels)])).float()  # wh label数据中wh*sacle

    def metric(k):  # compute metric
        r = wh[:, None] / k[None]  # lebel中wh 和 anchor的wh的比值 [bboxs, 9,2]
        # min(2)返回axis=2的最小值的矩阵和索引，即axis=2的维度消失。 [0]取最小值矩阵。 相当于取wh/wh_a和wh_a/wh的比值的最小值，再取w和h比值中的最小
        x = torch.min(r, 1. / r).min(2)[0]  # ratio metric [bboxs, 9]
        best = x.max(1)[0]  # best_x  label中一个bbox对应9个anchor，取这9个中的最大值得到最大的比值
        # label中所有bbox对应的9个anchor中h/h_a, w/w_a都要在(1/hyp['anchor_t'], hyp['anchor_t'])的个数
        aat = (x > 1. / thr).float().sum(1).mean()  # anchors above threshold
        # label中所有bbox对应的9个anchor中h/h_a, w/w_a的最大值在(1/hyp['anchor_t'], hyp['anchor_t'])的比例
        bpr = (best > 1. / thr).float().mean()  # best possible recall
        return bpr, aat

    bpr, aat = metric(m.anchor_grid.clone().cpu().view(-1, 2))
    print('anchors/target = %.2f, Best Possible Recall (BPR) = %.4f' % (aat, bpr), end='')
    if bpr < 0.98:  # threshold to recompute  原阈值0.98
        print('. Attempting to generate improved anchors, please wait...' % bpr)
        na = m.anchor_grid.numel() // 2  # number of anchors
        new_anchors = kmean_anchors(dataset, n=na, img_size=imgsz, thr=thr, gen=1000, verbose=False)
        new_bpr = metric(new_anchors.reshape(-1, 2))[0]
        if new_bpr > bpr:  # replace anchors
            new_anchors = torch.tensor(new_anchors, device=m.anchors.device).type_as(m.anchors)
            m.anchor_grid[:] = new_anchors.clone().view_as(m.anchor_grid)  # for inference
            m.anchors[:] = new_anchors.clone().view_as(m.anchors) / m.stride.to(m.anchors.device).view(-1, 1, 1)  # loss
            check_anchor_order(m)
            print('New anchors saved to model. Update model *.yaml to use these anchors in the future.')
        else:
            print('Original anchors better than new anchors. Proceeding with original anchors.')
    print('')  # newline


def check_anchor_order(m):
    # Check anchor order against stride order for YOLOv5 Detect() module m, and correct if necessary
    a = m.anchor_grid.prod(-1).view(-1)  # anchor area
    da = a[-1] - a[0]  # delta a
    ds = m.stride[-1] - m.stride[0]  # delta s
    if da.sign() != ds.sign():  # same order
        print('Reversing anchor order')
        m.anchors[:] = m.anchors.flip(0)
        m.anchor_grid[:] = m.anchor_grid.flip(0)


